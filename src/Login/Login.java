    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
  String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    if (username.equals("admin") && password.equals("1234")) {
        JOptionPane.showMessageDialog(this, "Admin Login Successful!");
        new Admin_Dashboard().setVisible(true);
        this.dispose();
    } else {
        User user;

        // Try HR Manager
        user = new HRManager(username, password);
        if (user.login()) {
            JOptionPane.showMessageDialog(this, "HR Manager Login Successful!");
            new HR_Manager_Dashboard().setVisible(true);
            this.dispose();
            return;
        }

        // Try HR Assistant
        user = new HRAssistant(username, password);
        if (user.login()) {
            JOptionPane.showMessageDialog(this, "HR Assistant Login Successful!");
            new HR_Assistant_Dashboard().setVisible(true);
            this.dispose();
            return;
        }

        // If none matched
        JOptionPane.showMessageDialog(this, "Invalid Username or Password!");
    }
    }//GEN-LAST:event_btnLoginActionPerformed

   
