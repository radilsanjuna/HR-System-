    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

                                 
    // Create HR_Manager object from text fields
    HR_Manager hr = new HR_Manager(
        jTextField1.getText(), // Manager Name
        jTextField3.getText(), // Username
        jTextField2.getText()  // Password
    );

    // Check for empty fields using getters
    if (hr.getManagerName().isEmpty() || hr.getUsername().isEmpty() || hr.getPassword().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all the fields!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection conn = DatabaseConnection.getConnection()) {
        // Check if username already exists
        PreparedStatement checkStmt = conn.prepareStatement("SELECT COUNT(*) FROM hr_manager WHERE username = ?");
        checkStmt.setString(1, hr.getUsername());
        ResultSet rs = checkStmt.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Username already exists. Please choose a different username.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Insert into DB using encapsulated object
        PreparedStatement insertStmt = conn.prepareStatement(
            "INSERT INTO hr_manager (hr_manager_name, username, password) VALUES (?, ?, ?)"
        );
        insertStmt.setString(1, hr.getManagerName());
        insertStmt.setString(2, hr.getUsername());
        insertStmt.setString(3, hr.getPassword());

        int rowsInserted = insertStmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "HR Manager added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saving HR Manager: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }



    }//GEN-LAST:event_saveActionPerformed

  
